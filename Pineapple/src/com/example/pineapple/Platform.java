package com.example.pineapple;

public class Platform {
	private int[] upperX;
	private int[] upperY;
	private int[] lowerX;
	private int[] lowerY;
	
	public Platform(int[] upperX, int[] upperY, int[] lowerX, int[] lowerY){
		this.upperX = upperX;
		this.upperY = upperY;
		this.lowerX = lowerX;
		this.lowerY = lowerY;
	}
	
	
}
